package com.example.diary.repository;

import com.example.diary.entity.User;

//Импорируем базовый интерфейс Spring Data JPA - JpaRepository.
//Это "родитель" нашего интерфейса, который предоставляет нам готовые
//методы: save(), findById(), findAll(), delete() и десятки других.
import org.springframework.data.jpa.repository.JpaRepository;

//Импорт контейнера Optional из стандартной библиотеки Java.
//Он нужен для безопасной обработки случаев, когда запрос к БД может ничего не найти.
import java.util.Optional;

//Объявляем интерфейс для работы с пользователями.
//Здесь не будет реализации — её создаст автоматически Spring Data JPA на этапе запуска приложения.
//Наследуем наш интерфейс от JpaRepository с двумя параметрами-типами:
//User — тип сущности, с которой работает репозиторий.
//Spring понимает, что все операции будут касаться таблицы, соответствующей классу User.
//Long — тип первичного ключа (id) сущности User.
//Благодаря этому наследованию мы получаем "из коробки":
//save(User entity) — сохранение нового или обновление существующего пользователя.
//findById(Long id) — поиск по первичному ключу, возвращает Optional<User>.
//findAll() — получение всех пользователей.
//delete(User entity) — удаление пользователя.
//count() — количество записей.
//и множество других методов без написания нами ни единой строки кода.
public interface UserRepository extends JpaRepository<User, Long> {

    //Декларируем метод для поиска пользователя по имени (логину).
    //JpaRepository сгенерирует запрос к БД:
    //SELECT * FROM users WHERE username = ?
    //Возвращаемый тип - Optional<User> — это означает, что пользователь может быть найден,
    //а может и отсутствовать, если в базе нет записи с таким именем, Optional будет пустым
    //(Optional.empty()). Это безопаснее, чем возвращать null, и заставляет того,
    //кто вызывает метод, явно обработать случай отсутствия пользователя.
    Optional<User> findByUsername(String username);

    //Декларируем метод для поиска пользователя по электронной почте.
    //Так же, как и у findByUsername: Spring парсит имя метода,
    //находит поле email в классе User, и генерирует SQL-запрос:
    //SELECT * FROM users WHERE email = ?
    //Optional<User> - потому что email уникален (благодаря unique = true в сущности),
    //но пользователь с таким email может отсутствовать в базе. Для этого
    //используется Optional, чтобы показать, что результата может и не быть.
    Optional<User> findByEmail(String email);
}